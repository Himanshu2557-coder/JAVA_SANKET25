public class datatypes {
    public static void main(String[] args) {
        // Java has several built-in data types, which can be categorized into two main groups:
        // 1. Primitive Data Types
        // 2. Reference Data Types
        
        // Primitive Data Types
        // These are the basic data types provided by Java.
        // They are not objects and hold their values directly.
        // The primitive data types in Java are:
        // byte, short, int, long, float, double, char, and boolean.
        
        int integerNumber = 42; 
        double decimalNumber = 3.14; 
        char character = 'A'; 
        boolean isTrue = true; 
        short shortNumber = 100; 
        long longNumber = 123456789L; 
        
        // Reference Data Types
        // These are data types that refer to objects or arrays.
        // They are not predefined in Java but can be created by the programmer.
        // Common reference data types include:
        // String , Arrays, Classes, and Interfaces.
        
        String text = "Hello, World!"; 
        
        // Print the values of the variables
        System.out.println("Integer: " + integerNumber);
        System.out.println("Double: " + decimalNumber);
        System.out.println("Character: " + character);
        System.out.println("Boolean: " + isTrue);
        System.out.println("String: " + text);
        System.out.println("Short: " + shortNumber);
        System.out.println("Long: " + longNumber);
    }
    
}
